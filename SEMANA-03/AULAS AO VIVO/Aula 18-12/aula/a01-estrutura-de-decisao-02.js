//ESTRUTURA DE DECISÃO SWITCH-CASE
var escolha = 1;

switch(escolha){
	case 1: console.log("Novo documento");

	case 2: console.log("Abrir documento");

	case 3: console.log("Salvar documento");

	case 4: console.log("Salvar como");

	default: console.log("Opção Inválida");
}

//USADO IF ELSE IF ELSE

if(esclha == 1){
	console.log("Novo documento");
}else if(escolha == 2){
	console.log("Abrir documento");
}else if(escolha == 3){
	console.log("Salvar documento");
}else if (escolha == 4){
	console.log("Salvar como");
}else{
	console.log("Opção Inválida");
}