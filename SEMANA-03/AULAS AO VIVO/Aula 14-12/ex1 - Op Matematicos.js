//OPERADORES MATEMÁTICOS
/*

(+) Soma
(-) Subtração
(*) Multiplicação
(/) Divisão
(%) Módulo ou Resto da divisão

*/

//INCREMENTOS
var numero = 10;

//Pré-Incremento
console.log(++numero);
//Pós-Incremento
console.log(numero++);

//DECREMENTOS
var valor = 20;

//Pré-Decremento
console.log(--valor);
//Pós-Incremento
console.log(valor++);

//Simplificando operações
var num;
//num = num + 1;
num +=1;
//num = num - 1;
num -=1;
//num = num * 1;
num *=1;
//num = num / 1;
num /=1;